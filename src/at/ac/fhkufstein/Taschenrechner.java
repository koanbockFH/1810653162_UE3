package at.ac.fhkufstein;

public class Taschenrechner
{
    public static double Addition(double zahl1, double zahl2)
    {
        return zahl1 + zahl2;
    }

    public static double Subtraktion(double zahl1, double zahl2)
    {
        return zahl1 - zahl2;
    }

    public static double Division(double zahl1, double zahl2)
    {
        return zahl1 / zahl2;
    }

    public static double Multiplikation(double zahl1, double zahl2)
    {
        return zahl1 * zahl2;
    }

    public static int Addition(int zahl1, int zahl2)
    {
        return zahl1 + zahl2;
    }

    public static int Subtraktion(int zahl1, int zahl2)
    {
        return zahl1 - zahl2;
    }

    public static int Division(int zahl1, int zahl2)
    {
        return zahl1 / zahl2;
    }

    public static int Multiplikation(int zahl1, int zahl2)
    {
        return zahl1 * zahl2;
    }
}
